const PptxGenJS = require("pptxgenjs");

async function main() {
  const pptx = new PptxGenJS();
  pptx.layout = "LAYOUT_16x9";
  pptx.author = "Codex PPTX Skill";
  pptx.company = "lin521045";
  pptx.subject = "Starter deck generated from codex-pptx-skill";
  pptx.title = "AI Assisted Research Writing Workflow";
  pptx.lang = "en-US";

  function addTitleBar(slide, title, subtitle, dark = true) {
    slide.background = { color: dark ? "1E2761" : "F6F4EF" };
    slide.addText(title, {
      x: 0.7,
      y: 0.55,
      w: 8.6,
      h: 0.6,
      fontFace: "Georgia",
      fontSize: 24,
      bold: true,
      color: dark ? "FFFFFF" : "1E2761",
      margin: 0,
    });
    if (subtitle) {
      slide.addText(subtitle, {
        x: 0.72,
        y: 1.15,
        w: 7.8,
        h: 0.4,
        fontFace: "Calibri",
        fontSize: 11,
        color: dark ? "D9E4FF" : "50627A",
        italic: true,
        margin: 0,
      });
    }
  }

  function addCard(slide, { x, y, w, h, accent, title, body }) {
    slide.addShape(pptx.ShapeType.rect, {
      x,
      y,
      w,
      h,
      line: { color: "E3E0D8", width: 1 },
      fill: { color: "FFFFFF" },
      shadow: { type: "outer", color: "000000", blur: 2, angle: 45, offset: 1, opacity: 0.1 },
    });
    slide.addShape(pptx.ShapeType.rect, {
      x,
      y,
      w: 0.12,
      h,
      line: { color: accent, width: 0 },
      fill: { color: accent },
    });
    slide.addText(title, {
      x: x + 0.28,
      y: y + 0.22,
      w: w - 0.45,
      h: 0.32,
      fontFace: "Georgia",
      fontSize: 15,
      bold: true,
      color: "243447",
      margin: 0,
    });
    slide.addText(body, {
      x: x + 0.28,
      y: y + 0.62,
      w: w - 0.45,
      h: h - 0.82,
      fontFace: "Calibri",
      fontSize: 11,
      color: "4D5C6B",
      margin: 0,
      valign: "top",
    });
  }

  const title = pptx.addSlide();
  addTitleBar(title, "AI Assisted Research Writing Workflow", "Starter deck generated by codex-pptx-skill", true);
  title.addText("From fragmented tools to a guided workflow", {
    x: 0.75,
    y: 2.05,
    w: 5.6,
    h: 0.9,
    fontFace: "Georgia",
    fontSize: 28,
    bold: true,
    color: "FFFFFF",
    margin: 0,
  });
  title.addText("Good for briefings, defenses, method walkthroughs, and product storytelling.", {
    x: 0.78,
    y: 3.05,
    w: 4.8,
    h: 0.6,
    fontFace: "Calibri",
    fontSize: 14,
    color: "D9E4FF",
    margin: 0,
  });
  title.addShape(pptx.ShapeType.rect, {
    x: 7.1,
    y: 1.3,
    w: 2.0,
    h: 2.0,
    fill: { color: "CADCFC", transparency: 15 },
    line: { color: "CADCFC", width: 0 },
  });
  title.addText("8 slides\nStructured\nQA-first", {
    x: 7.38,
    y: 1.72,
    w: 1.4,
    h: 1.2,
    fontFace: "Consolas",
    fontSize: 18,
    bold: true,
    color: "1E2761",
    align: "center",
    valign: "mid",
    margin: 0,
  });

  const overview = pptx.addSlide();
  addTitleBar(overview, "Workflow Overview", "Use cards instead of dense bullet pages", false);
  addCard(overview, {
    x: 0.7,
    y: 1.7,
    w: 2.75,
    h: 1.45,
    accent: "1E2761",
    title: "1. Gather inputs",
    body: "Collect the brief, template deck, data, and must-keep conclusions before layout work starts.",
  });
  addCard(overview, {
    x: 3.65,
    y: 1.7,
    w: 2.75,
    h: 1.45,
    accent: "B85042",
    title: "2. Choose path",
    body: "Decide between template editing and from-scratch generation based on visual reuse value.",
  });
  addCard(overview, {
    x: 6.6,
    y: 1.7,
    w: 2.75,
    h: 1.45,
    accent: "84B59F",
    title: "3. QA and revise",
    body: "Render slides, check placeholders, fix spacing and contrast issues, then verify again.",
  });

  const structure = pptx.addSlide();
  addTitleBar(structure, "Recommended Slide Mix", "A starter outline for an 8-page briefing deck", false);
  structure.addText([
    { text: "01  ", options: { bold: true, color: "1E2761" } },
    { text: "Title and promise", options: { breakLine: true } },
    { text: "02  ", options: { bold: true, color: "1E2761" } },
    { text: "Problem and context", options: { breakLine: true } },
    { text: "03  ", options: { bold: true, color: "1E2761" } },
    { text: "Current workflow pain points", options: { breakLine: true } },
    { text: "04  ", options: { bold: true, color: "1E2761" } },
    { text: "Proposed workflow", options: { breakLine: true } },
    { text: "05  ", options: { bold: true, color: "1E2761" } },
    { text: "Evidence or result snapshots", options: { breakLine: true } },
    { text: "06  ", options: { bold: true, color: "1E2761" } },
    { text: "Implementation timeline", options: { breakLine: true } },
    { text: "07  ", options: { bold: true, color: "1E2761" } },
    { text: "Risks and mitigations", options: { breakLine: true } },
    { text: "08  ", options: { bold: true, color: "1E2761" } },
    { text: "Closing takeaway", options: {} },
  ], {
    x: 0.9,
    y: 1.55,
    w: 4.2,
    h: 3.6,
    fontFace: "Calibri",
    fontSize: 17,
    color: "334155",
    margin: 0,
  });
  structure.addShape(pptx.ShapeType.rect, {
    x: 5.6,
    y: 1.65,
    w: 3.1,
    h: 2.8,
    fill: { color: "EAF0FF" },
    line: { color: "CADCFC", width: 1 },
  });
  structure.addText("Design reminder", {
    x: 5.9,
    y: 2.0,
    w: 2.2,
    h: 0.3,
    fontFace: "Georgia",
    fontSize: 18,
    bold: true,
    color: "1E2761",
    margin: 0,
  });
  structure.addText("Alternate layouts.\nUse one dominant color.\nDo not ship the first export without QA.", {
    x: 5.9,
    y: 2.45,
    w: 2.2,
    h: 1.2,
    fontFace: "Calibri",
    fontSize: 13,
    color: "44576A",
    margin: 0,
  });

  await pptx.writeFile({ fileName: "starter-deck.pptx" });
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
